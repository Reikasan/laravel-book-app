<?php

namespace Database\Factories;

use Illuminate\Database\Eloquent\Factories\Factory;

/**
 * @extends \Illuminate\Database\Eloquent\Factories\Factory<\App\Models\Model>
 */
class BookFactory extends Factory
{
    /**
     * Define the model's default state.
     *
     * @return array<string, mixed>
     */
    public function definition(): array
    {
        return [
            'title' => $this->faker->sentence($this->faker->numberBetween(1, 4)),
            'sub_title' => $this->faker->sentence($this->faker->numberBetween(1, 10)),
            'authors' => $this->faker->name(),
            'description' => $this->faker->paragraph($this->faker->numberBetween(1, 10)),
            'image_link' => $this->faker->imageUrl(),
            'isbn' => $this->faker->isbn10(),
            'isbn13' => $this->faker->isbn13(),
            'language' => $this->faker->languageCode(),
            'page_count' => $this->faker->numberBetween(50, 1000),
            'publisher' => $this->faker->company(),
            'published_date' => $this->faker->date(),
            'category_id' => $this->faker->numberBetween(1, 10),
            'google_book_id' => $this->faker->numberBetween(1, 100000),
        ];
    }
}
